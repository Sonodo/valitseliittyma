// Helper to get plans by operator
export function getPlansByOperator(operatorId: string): MobilePlan[] {
  return mobilePlans.filter((p) => p.operatorId === operatorId);
}

// Helper to get cheapest plans
export function getCheapestPlans(limit: number = 5): MobilePlan[] {
  return [...mobilePlans].sort((a, b) => a.monthlyPrice - b.monthlyPrice).slice(0, limit);
}

// Helper to get 5G plans
export function get5GPlans(): MobilePlan[] {
  return mobilePlans.filter((p) => p.has5G);
}
